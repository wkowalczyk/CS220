/* 
 * replaceByte(x,n,c) - Replace byte n in x with c
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: replaceByte(0x12345678,1,0xab) = 0x1234ab78
 *   You can assume 0 <= n <= 3 and 0 <= c <= 255
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 3
 */
int replaceByte(int x, int n, int c) {
  /*take the byte that needs to be changed and move it all the way to the least 
  *significant byte. Store it as int a. 
  *And it with 0xff so that any byte before it is 0x00. xor that with the byte it needs
  *to change into to get the bits that toggle. Move that byte (int d) back to it's original place
  *and xor it with the original number to get the new number.*/
  int a = (x >> (n << 3));
  int b = a & 0xff;
  int d = b ^ c;
  int e = d << (n << 3);
  return e ^ x;
                   
}
/* 
 * logicalShift - shift x to the right by n, using a logical shift
 *   Can assume that 0 <= n <= 31
 *   Examples: logicalShift(0x87654321,4) = 0x08765432
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3 
 */
int logicalShift(int x, int n) {
/*make a mask and bit shift it n-1 times to the right.
 *And it with x shifted over n times.
 *Check to see if n is 0 or any other number by taking the two's complement and
 *negating it. If it is 0, return x. If it is any other number, return the newX.*/
  int a = (0x01) << 31;
  int b = ~(a >> ((n + ((~(0x01))+1))));
  int newX = ((x >> n) & b);
  /*check to see if n is 0*/
  int checkN = ~(((~n)+1) >> 31);
  return (newX & ~checkN) | (x & checkN);
}
