/* 
 * sm2tc - Convert from sign-magnitude to two's complement
 *   where the MSB is the sign bit
 *   Example: sm2tc(0x80000005) = -5.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 4
 */
int sm2tc(int x) {
/*if number is positive, return x. Otherwise keep the sign bit, complement the other bits and add 1.*/
  int ifPos = (x >> 31);
  int retainSignBit = ((x >> 31) << 31);
  int y = ((~x) + 1) + retainSignBit;
  return (x & ~ifPos) | (y & ifPos);
}
